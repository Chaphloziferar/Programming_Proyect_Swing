    private void BusquedaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BusquedaActionPerformed
        if (Dato.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Debe llenar el formulario", "ERROR", JOptionPane.ERROR_MESSAGE);
        } else {
            LinkedList<Persona> buscado = buscarPersona();
            if (buscado == null) {
                JOptionPane.showMessageDialog(null, "No se encontró ningún resultado", "Mensaje", JOptionPane.INFORMATION_MESSAGE);
            } else {
                Ventana_Buscar vb = new Ventana_Buscar();
                vb.p = buscado.getFirst();
                vb.setVisible(true);
                vb.obtenerDatos();
                this.setVisible(false);
            }
        }
    }//GEN-LAST:event_BusquedaActionPerformed

