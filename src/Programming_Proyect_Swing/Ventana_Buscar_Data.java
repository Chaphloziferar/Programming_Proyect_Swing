    public LinkedList<Persona> buscarPersona() {
        int pos = -1;
        String dato = Dato.getText();
        LinkedList<Persona> search_result = new LinkedList<>();

        if (Nombre_op.isSelected() == true) {
            for (Persona p : Ventana_Agregar.Lista) {
                if (p.getNombre().equalsIgnoreCase(dato)) {
                    pos = Ventana_Agregar.Lista.indexOf(p);
                    search_result.add(Ventana_Agregar.Lista.get(pos));
                }
            }

            if (pos == -1) {
                search_result = null;
            }
        }

        if (Codigo_op.isSelected() == true) {
            for (Persona p : Ventana_Agregar.Lista) {
                if (p.getCodigo().equalsIgnoreCase(dato)) {
                    pos = Ventana_Agregar.Lista.indexOf(p);
                    search_result.add(Ventana_Agregar.Lista.get(pos));
                }
            }

            if (pos == -1) {
                search_result = null;
            }
        }

        return search_result;

    }

