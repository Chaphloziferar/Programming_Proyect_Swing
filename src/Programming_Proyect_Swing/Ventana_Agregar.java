    private void RegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RegistrarActionPerformed
        try {
            if (Nombre.getText().isEmpty()) {
                e1 = true;
                throw new NullPointerException();
            } else {
                nombre = Nombre.getText();
            }
            if (Consola.getText().isEmpty()) {
                e2 = true;
                throw new NullPointerException();
            } else {
                consola = Consola.getText();
            }
            if (Edad.getText().isEmpty()) {
                e3 = true;
                throw new NullPointerException();
            } else {
                edad = Integer.parseInt(Edad.getText());
            }
            if (Fecha.getDate() == null) {
                e4 = true;
                throw new NullPointerException();
            } else {
                Date f1 = Fecha.getDate();
                DateFormat f = new SimpleDateFormat("dd/MM/yyyy");
                fecha = f.format(f1);
            }
            if (Comentario.getText().isEmpty()) {
                e5 = true;
                throw new NullPointerException();
            } else {
                comentario = Comentario.getText();
            }
            if (Precio.getText().isEmpty()) {
                e6 = true;
                throw new NullPointerException();
            } else {
                try {
                    precio = Double.parseDouble(Precio.getText());
                } catch (InputMismatchException e) {
                    e6 = true;
                }
            }
            if (Codigo.getText().isEmpty()) {
                e7 = true;
                throw new NullPointerException();
            } else {
                codigo = Codigo.getText();
            }
            
            persona = new Persona(nombre, edad, consola, precio, fecha, comentario, codigo);
            Lista.add(persona);
            
            try {
                ObjectOutputStream oos = new ObjectOutputStream(new FileOutputStream("Personas.dat"));
                oos.writeObject(Lista);
                oos.close();
                JOptionPane.showMessageDialog(null, "Datos guardados exitosamente", "Mensaje", JOptionPane.INFORMATION_MESSAGE);
                this.setVisible(false);
                Ventana_Principal v = new Ventana_Principal();
                v.setVisible(true);
            } catch (IOException e) {
                JOptionPane.showMessageDialog(null, "Ha ocurrido un error", "ERROR", JOptionPane.ERROR_MESSAGE);
            }
            
        } catch (InputMismatchException | NumberFormatException | NullPointerException e) {
            if (e1 == true) {
                JOptionPane.showMessageDialog(this, "Debe escribir el nombre completo del cliente");
                e1 = false;
            }
            if (e2 == true) {
                JOptionPane.showMessageDialog(this, "Debe escribir el nombre de la videoconsola vendida");
                e2 = false;
            }
            if (e3 == true) {
                JOptionPane.showMessageDialog(this, "Debe escribir la edad del cliente");
                e3 = false;
            }
            if (e4 == true) {
                JOptionPane.showMessageDialog(this, "Debe escribir la fecha de venta");
                e4 = false;
            }
            if (e5 == true) {
                JOptionPane.showMessageDialog(this, "Debe escribir un comentario del cliente");
                e5 = false;
            }
            if (e6 == true) {
                JOptionPane.showMessageDialog(this, "Debe escribir un precio valido");
                e6 = false;
            }
            if (e7 == true) {
                JOptionPane.showMessageDialog(this, "Debe escribir el codigo de registro");
                e7 = false;
            }
        }
    }//GEN-LAST:event_RegistrarActionPerformed
    
